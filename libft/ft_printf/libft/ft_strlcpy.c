#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	int		src_len;
	size_t	i;

	src_len = 0;
	while (src[src_len])
		src_len++;
	i = 0;
	if (size == 0)
		return (src_len);
	while (src[i] != '\0' && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (src_len);
}
