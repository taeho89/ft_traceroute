#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*new_node;
	void	*cont_buf;

	new_list = NULL;
	while (lst != NULL)
	{
		cont_buf = f(lst->content);
		new_node = ft_lstnew(cont_buf);
		if (new_node == NULL)
		{
			del(cont_buf);
			ft_lstclear(&new_list, del);
			return (NULL);
		}
		ft_lstadd_back(&new_list, new_node);
		lst = lst->next;
	}
	return (new_list);
}
